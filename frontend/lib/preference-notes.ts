/** 词典外兜底模板——理论上不该触发（top_priors 只出自受控词典），留作防线。 */
function _fallbackNote(tag: string): string {
  return `你最近偏爱${tag}这类安排`;
}

/**
 * 「偏好」子项笔记——取 `topPriors` 最靠前的 1 个 tag 生成一句。
 * 空数组时返回 null（由调用方决定是否渲染该行/走空态）。
 */
export function preferenceNote(topPriors: string[]): string | null {
  const top = topPriors[0];
  if (!top) return null;
  return _TAG_NOTE[top] ?? _fallbackNote(top);
}

/**
 * 「距离」子项笔记——按建议距离生成一句，口语、无计数（公里数本身是尺度
 * 描述不是"计数"，符合"无计数数字"是指不展示"×N 次"这类频次统计）。
 */
export function distanceNote(suggestedKm: number | null | undefined): string | null {
  if (suggestedKm == null) return null;
  const km = Number.isInteger(suggestedKm) ? suggestedKm : Math.round(suggestedKm * 10) / 10;
  return `最近都定在 ${km}km 内`;
}
