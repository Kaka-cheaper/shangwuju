"""agent.core.coverage_gate —— per-rule 覆盖度闸（对话轮路由规则层重构，2026-07-12）。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
【这是什么问题】

  级联分类（cascaded classification）里，规则层"命中即拍板、无兜底"，代价是
  假阳性——句子里除了触发词还有别的实义内容时，规则层单靠关键词命中就拍板，
  会把"含反馈词但其实在问 / 在提别的事"的句子错误地短路掉。

  成熟范式：规则层短路的前提不该只是"含触发词"，而是"整句就是这个触发词
  （+ 允许的功能词垫场），没有其他实义内容"——即**覆盖度**判据：锚点命中的
  片段 + 允许的填充词，覆盖了整句，才有资格短路；有覆盖不到的残余，说明句子
  里还有规则层读不懂的东西，必须弃权交给能读懂上下文的判决者（本仓库是路由
  脑子 `agent.routing.brain`）。

  这不是新发明——是"规则命中但保留兜底"和"契约测试钉死输入空间"两个思路的
  合流：规则层各自的词表继续负责"哪些锚点算数"，但"这句话是否只由锚点+填充
  词构成"从各条规则各写一套排除逻辑（confirm 的"纯肯定否则交回"、booking 的
  4 道排除、feedback_strong 的 `_STRONG_SCAN_NOISE` 剔噪）收口成一个共享判据。

【为什么不做"多类冲突闸"】

  单一覆盖度闸已结构性吸收跨类冲突：两条互斥规则（如 booking 与 confirm）
  不可能同时把同一句话的残余都归零——覆盖度闸要求"这条规则自己的锚点 + 填充词
  ≈ 整句"，一句话如果真的同时像两种意图，必然至少有一条规则会因为对方专属的
  词覆盖不到而弃权。不需要在覆盖度闸之外再叠一层"多类都命中就仲裁"的机制。

【填充集是什么、为什么冻结】

  填充集 = 汉语里最基本、不携带判别语义的语法功能词核心：
      了 / 的 / 吧 / 呢 / 啊 / 呀 / 嘛（语气助词）
      一下 / 一点（弱化助词短语）
      + 中英文标点
  这些词在任何一种路由标签下都不改变句子的判别方向——"太远了" 和 "太远" 判定
  应该一致，"吧" 加不加也不该影响"就这个"是不是确认。

  铁律：这个集合**冻结**，以后任何"规则误判"都不许通过往填充集里加词来"堵漏"
  ——那正是历史上 `_STRONG_SCAN_NOISE` 这类 per-word 补丁滋生的路子（先是给
  "附近"打补丁防"近点"误命中，下一次遇到新碰撞又加一个词，补丁越滚越大、
  谁也说不清覆盖边界）。填充集只通过枚举语言学上真正空义的语法词冻结一次；
  新的误判一律通过**扩大对应规则自己的锚点词表**（如 booking 给"预约"补上
  "给我预约"复合锚点）或**转交 brain 少样本**解决，不动填充集本身。

  灰区词（想/有点/稍微/但是/程度词/地名/具体名词如过敏原名称）一律不进填充集
  ——它们是"残余实义"，命中即弃权，交给带上下文的脑子判断，这是**设计选择**
  不是遗漏（见 `agent/core/feedback_detector.py` "过敏" 一词的模块级说明：
  "我海鲜过敏" 这类点名过敏原的真实句子，覆盖度闸下会因为过敏原名词是非空
  残余而弃权到脑子——这是本闸机制的直接推论，不是本批引入的新 bug）。

【二值判定】

  只问"残余是否为空"，不做模糊评分——级联分类规则层要的是判错代价可控的
  确定性短路，不是"大概率对"的软判据。
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
"""

from __future__ import annotations

# ============================================================
# 冻结填充集（铁律：只能读，不能加词——见模块 docstring）
# ============================================================

FILLER_WORDS: frozenset[str] = frozenset(
    {
        # 语气助词
        "了", "的", "吧", "呢", "啊", "呀", "嘛",
        # 弱化助词短语
        "一下", "一点",
    }
)
"""9 个冻结硬功能词（语气助词 7 + 弱化助词短语 2），加中英标点合计约 12 个
填充记号——见模块 docstring「填充集是什么、为什么冻结」。"""

FILLER_PUNCTUATION: frozenset[str] = frozenset("，。！？!?~～…、,.:：;；\t\r\n ")
"""中英文标点 + 空白——同样不携带判别语义，覆盖度计算前一并剔除。"""


def _strip_fillers(text: str) -> str:
    out = text
    # 长词先剔，避免"一下"被拆成"一"+"下"两次误剔导致误伤（本集合内互不为
    # 子串，这里仅为纪律一致性，不影响当前 9 词的正确性）。
    for w in sorted(FILLER_WORDS, key=len, reverse=True):
        out = out.replace(w, "")
    return "".join(ch for ch in out if ch not in FILLER_PUNCTUATION)


def covers(text: str, anchors: tuple[str, ...]) -> bool:
    """覆盖度闸主判据：锚点片段 ∪ 填充集，是否覆盖了整句（残余是否为空）。

    Args:
        text:    原始整句（调用方需自行 strip，不在此处重复裁剪首尾）。
        anchors: 这条规则**自己**命中的锚点子串（如 feedback_strong 命中的
                 "过敏"、confirm 命中的"好的"+"就这个"）。允许传入多个——
                 一句话可能同时命中同一规则词表的多个锚点词，全部凿掉才计算
                 残余（如"太远了，近点"命中"太远"和"近点"两个锚点）。

    Returns:
        True  → 覆盖完整，规则层可以拍板短路；
        False → 有残余实义内容，规则弃权，交回上层继续级联（通常落到脑子）。

    二值判定，不做模糊评分——见模块 docstring。空 anchors（规则本身未命中）
    视为不覆盖（False），调用方正常不会在未命中时调用本函数，这里只是防御。
    """
    if not anchors:
        return False
    residual = text
    for a in sorted(anchors, key=len, reverse=True):
        residual = residual.replace(a, "")
    residual = _strip_fillers(residual)
    return residual == ""


__all__ = ["FILLER_WORDS", "FILLER_PUNCTUATION", "covers"]
