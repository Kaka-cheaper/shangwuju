"""agent.routing.brain_prompt —— 统一路由脑子的 system prompt + 少样本（ADR-0011 E-2-c）。

只装 prompt 文案，不装调用逻辑（调用在 `brain.py`）。

【为什么是这份 prompt，不是别的】
ADR-0011 决策 1/2：路由标签闭集 6 类 = L0 响应义务契约的路由投影，一次 LLM 调用
同时产出「标签 + 槽位 + 置信度」，取代旧 `classify_input`（6 旧类）+
`classify_dialogue_act`（会话内对话行为）两次调用、两套词汇。旧 prompt
（`agent/intent/prompts/router_prompt.py` 的 `ROUTER_SYSTEM_PROMPT`）随 Layer 2
退役——它的 6 类（planning/chitchat/meta/emotional/off_topic/ambiguous）是「输入
表面特征」分类，新 6 类是「系统欠用户哪种响应义务」分类（L0 契约原文："按
『系统欠用户什么反应』分类，不按输入特征"），这不是简单改名，是分类轴的转变，
故 prompt 需整篇重写，不是微调旧文案。

【八场景少样本怎么落地】
ADR-0011 决策 2 原文："八个预设场景与路由的结合方式：场景作为少样本范例进
prompt……不是 if social_context 分支"。下面 `BRAIN_FEW_SHOTS` 里专门有一组
对照样本——同一句「有点累了」在（a）已经是宽松独处方案（S8 风格）下判 chitchat
（共情但不擅自动方案），（b）行程本就紧凑的多人局（S2 风格）下判 feedback
（明确指向"节奏"这个可调参数）——让 LLM 学会依上下文调整判断，而不是靠代码
分支写死"含『累』字 → X"。
"""

